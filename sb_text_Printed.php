<p>
This series of works grew out of an interest in early wood block printing techniques and crude forms of engraving. In these paintings I have literally transposed the physical process of intaglio engraving onto a wax primed surface. Some pieces actually use reverse intaglio where the image is gouged out of the surface and then revealed through its flooding with pigmented wax, and others apply an individually cut lino or wood block print which is stamped into and repeated across the whole surface of the painting (as with the Muybridge piece.)
