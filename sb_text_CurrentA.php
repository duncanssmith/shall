…a looping, meandering line which seems at first to have escaped the grid’s influence entirely […] emerges as an expressive agent, its progress improvised intuitively to follow the path of a rhizoid growth within a schematic system […] becoming in places partly decorative or ornamental, in the musical sense, as it is wrought into baroque arabesques and curlicues which intersperse the loose linear sweeps. Instead of two irreconcilable forms, set in sterile opposition, the grid and line begin to interact as the work progresses.
</p>
<p>
David Sweet
