<h4>Grids</h4>
<blockquote>
<p>
In 2003 I took the decision to remove the paintings from the confines of the imposed grid which I had begun to feel had exhausted its usefulness. The new paintings began to make a freer and more improvisatory use of the ornamental and appropriated arabesque flourishes I had hitherto used. The resulting work was seeking to redefine the idea of the autonomous and self referential simplicity of a singular line, transcribed Sol Lewitt like through a very direct and economically straightforward method whilst at the same time animating through an ergonomic relationship, my involvement in its facture.
</p>
</blockquote>
