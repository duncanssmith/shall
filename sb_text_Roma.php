<p>
This small series of paintings were made after my return from Italy in 1991 where I had been on a Rome Scholarship. They use fragments of images taken from wall paintings which I had seen decorating the various underground catacombs within the city itself. These images are then juxtaposed with a single, monochrome, painterly field in an oblique reference to both the marking out of time and its recontextualisation in the present; to both "the forgetfulness of the modernist autonomous object and the attempt to hold on to a possibility of retrieving some form of truth content from the distant past." (<i>David Ryan</i>)
</p>




