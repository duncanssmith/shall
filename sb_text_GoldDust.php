<p>
The American composer, Christian Wolff and in particular his small chamber work Star Dust Pieces was a starting point for this series of works. I was extremely fascinated by the seemingly esoteric and highly abstract nature of Wolff’s music and yet his absolute commitment to socialist politics (which in those days more often than not resulted in highly programmatic and ‘didactic’ musical forms).This ‘conflict’(if it could be termed as such) paralleled something of my own dilemma within a practice in which  a developed sense of  progressive, formal developments remained paramount  at the same time as did an underlying concern with social change and some of modernism’s more utopian concepts. 
</p>





