<h5>Fabian Peake, Sharon Hall, Painting, Islington Mill Manchester 2005</h5>

<blockquote>
<p>Sharon Hall’s new paintings exemplify a particular approach to the installation of painting and the questioning of the relationship between individual surfaces. The individual surfaces present multiple contradictions between ground and mark, form and the formless, order and chaos...</p>
<p>
It is not a self critical definition of painting but rather a mapping between different contradictory elements, emphasising the dualistic approach, or a shadow play, which through the method of installation becomes more apparent. In essence the actual installation constructs an ‘in between’ where connections are made between and across the different paintings.
</p>
</blockquote>
<p><i>Alistair Payne, catalogue essay, June 2005</i></p>

